    PRICE_LIST BIGINT PRIMARY KEY,
    PRODUCT_ID BIGINT NOT NULL, 
    START_DATE TIMESTAMP,
    END_DATE TIMESTAMP,
    BRAND_ID BIGINT,
    PRIORITY BIGINT,
    PRICE REAL,
    CURR VARCHAR(50)
);

